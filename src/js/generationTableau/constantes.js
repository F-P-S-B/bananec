var createTable = (id, liste) => {
  const rootElt = document.getElementById(id);
  const h1rootElt = document.createElement("h1");
  h1rootElt.innerText = id;
  rootElt.appendChild(h1rootElt);

  const tabElt = document.createElement("table");

  let tempStr = `<tr>
                <th>Objet</th>
                <th>Unité</th>
                <th>Qté</th>
                <th>Prix</th>
              </tr>`;
  for (obj of liste) {
    tempStr += `<tr class="more-info">
                <td
                  data-description="${obj.description}"
                  data-img="${obj.photo}"
                  data-don="${obj.lienDon}"
                  >
                  ${obj.nom}
                  </td>
                  <td> ${obj.unite}</td>
                <td>${obj.qte}</td>
                <td>${parseInt(obj.prix)}€</td>
              </tr>`;
  }
  tabElt.innerHTML = tempStr;
  rootElt.appendChild(tabElt);
};
